    public boolean buscarpersona(String cedula) {
        boolean esta = false;

        for (int i = 0; i < datospersonales.size(); i++) {
            per = (Persona) datospersonales.get(i);
            if (per.getCedula().equals(cedula)) {
                esta = true;
                break;
            }
        }

        return esta;
    }

