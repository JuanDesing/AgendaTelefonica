    private void lineatelefonicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lineatelefonicaActionPerformed

        String operadorclaro = "";
        String operadormovistar = "";
        for (int i = 0; i < datospersonales.size(); i++) {

            per = (Persona) datospersonales.get(i);

            int ultimostresnumeros = (int) (per.getTelefono() / 10000000);

            if (ultimostresnumeros == 310 || ultimostresnumeros == 311 || ultimostresnumeros == 312
                    || ultimostresnumeros == 313 || ultimostresnumeros == 314 || ultimostresnumeros == 320
                    || ultimostresnumeros == 321 || ultimostresnumeros == 322 || ultimostresnumeros == 323) {

                JOptionPane.showMessageDialog(null, "probando 123");
                operadorclaro += per.getNombre() + "\n";
            } else if (ultimostresnumeros == 315 || ultimostresnumeros == 316 || ultimostresnumeros == 316
                    || ultimostresnumeros == 317 || ultimostresnumeros == 318) {
                operadormovistar += per.getNombre() + "\n";
            }

        }

        movistar.setText(operadormovistar);
        claro.setText(operadorclaro);
    }//GEN-LAST:event_lineatelefonicaActionPerformed

