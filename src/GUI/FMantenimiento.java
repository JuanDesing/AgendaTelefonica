    private void agregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar1ActionPerformed
        if (cedula.getText().isEmpty() || nombre.getText().isEmpty() || telefono.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Complete el espacio vacio!!");

        } else {

            if (!buscarpersona(cedula.getText())) {
                if (telefono.getText().length() != 10) {
                    JOptionPane.showMessageDialog(null, "el número telefónico no es válido,"
                            + "por favor verificar el número");

                    telefono.setText("");
                } else {

                    Persona p1 = new Persona(nombre.getText().trim(), cedula.getText().trim(),
                            Long.parseLong(telefono.getText().trim()), correo.getText());
                    datospersonales.add(p1);
                    JOptionPane.showMessageDialog(null, "¡ Persona agendada !");
                    cedula.setText("");
                    nombre.setText("");
                    telefono.setText("");
                    correo.setText("");

                    nombre.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cédula ya registrada \nVerifique la cédula");
                cedula.setText("");
                cedula.requestFocus();
            }

        }
    }//GEN-LAST:event_agregar1ActionPerformed

    private void buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar1ActionPerformed

        String ced = JOptionPane.showInputDialog(null, "Digite la cedula de la persona que quieres buscar: ");
        //JOptionPane.showMessageDialog(null, "" + datospersonales.get(0));
        if (buscarpersona(ced)) {
            nombre.setText(per.getNombre());
            cedula.setText(per.getCedula());
            telefono.setText(String.valueOf(per.getTelefono()));
            correo.setText(per.getCorreo());
        } else {
            JOptionPane.showMessageDialog(null, "No existe esta persona!");
        }


    }//GEN-LAST:event_buscar1ActionPerformed

